public class AuraDetectorItem extends AuraItem {
    public AuraDetectorItem(Settings settings) {
        super(settings);
    }

    @Override
    public TypedActionResult<ItemStack> use(World world, PlayerEntity player, Hand hand) {
        ItemStack itemStack = player.getStackInHand(hand);
        if (!world.isClient()) {
            player.sendMessage(Text.literal("You used the item!"), false);
            player.sendMessage(Text.literal("Your aura count currently is: " + calculateAura(player)), false);

        }

        return TypedActionResult.success(itemStack);
    }

}
